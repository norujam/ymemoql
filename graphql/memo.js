import mongoose from 'mongoose';

const MemoSchema = new mongoose.Schema({
    id: {
        type: Number,
        required: true,
        unique: true,
    },
    title: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: true,
    }
});
export default mongoose.model('Memo', MemoSchema);